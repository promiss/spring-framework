package com.infuq.springframe;

public class Computer {

	private String name = "HP";

	private int year = 2020;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}
}
